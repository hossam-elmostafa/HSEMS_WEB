/**
 * Desktop parity (HSEMSCommonCategory ID_NEWCARMODULE_CARENTRY):
 * If user department != owner department, apply WHERE CRENTRY_DPR='<userDept>' on CAR Entry list.
 */
export async function onMenuItemClicked(userObj, strScrTag, callBackFn) {
  const tag = String(strScrTag || '').toUpperCase().trim();
  if (tag !== 'HSE_TGCRENTRY') {
    callBackFn({ isAllowed: true, strScrCriteria: '', strMsg: '' });
    return;
  }

  const dev = getDevInterface() || {};
  const ownerDept = await getOwnerDepartment(dev);
  const userDept = await getUserDepartment(dev, userObj);

  let strScrCriteria = '';
  if (userDept && ownerDept && userDept !== ownerDept) {
    strScrCriteria = `WHERE CRENTRY_DPR='${escSql(userDept)}'`;
  }

  callBackFn({ isAllowed: true, strScrCriteria, strMsg: '' });
}
