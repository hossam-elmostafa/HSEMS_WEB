// RQ_HSE_4_4_25_18_22 — toolbar: NEW defaults, SAVE desktop parity (pre/post)
export async function toolBarButtonClicked(eventObj, callBackFn) {
  const devInterface = eventObj.devInterfaceObj || getDevInterface() || {};
  const strTabTag = (eventObj.strTabTag && eventObj.strTabTag.toString().toUpperCase()) || '';
  const strBtnName = (eventObj.strBtnName && eventObj.strBtnName.toString().toUpperCase()) || '';
  const complete = eventObj.complete;
  const completeStr = complete == null ? '' : String(complete).trim();
  const isCarSavePostPhase = complete === 1 || completeStr === '1';
  /** Pre phase: falsy 0/''/false/undefined or explicit '0' (string '0' is truthy to `!complete`). */
  const isCarSavePrePhase =
    !isCarSavePostPhase &&
    (complete == null ||
      complete === '' ||
      complete === 0 ||
      complete === false ||
      completeStr === '0');
  const scrUpper = String(eventObj.strScrTag || '').toUpperCase();

  try {
    if (strBtnName === 'NEW' && isCarSavePostPhase && !strTabTag && scrUpper === 'HSE_TGCRENTRY') {
      await applyNewCarEntryMainDefaults(devInterface, eventObj);
    }
  } catch (e) {
    console.warn('[Web_HSE] CAR Entry NEW main defaults:', e);
  }

  try {
    if (strBtnName === 'SAVE' && scrUpper === 'HSE_TGCRENTRY' && !strTabTag) {
      if (isCarSavePrePhase) {
        await carEntryDesktopSave.preSave(devInterface, eventObj);
      } else if (isCarSavePostPhase) {
        await carEntryDesktopSave.postSave(devInterface, eventObj);
      }
    }
  } catch (e) {
    console.warn('[Web_HSE] CAR Entry SAVE desktop parity:', e);
  }

  // RQ_HSE_12_4_26_00_40 — C++: block DELETE when CRENTRY_ATGNR = Y (toolbar may use pre/post phases)
  if (strBtnName === 'DELETE' && !strTabTag && scrUpper === 'HSE_TGCRENTRY') {
    const blocked = await shouldBlockDeleteAutoGeneratedCar(devInterface, eventObj);
    if (blocked) return;
  }

  if (typeof callBackFn === 'function') callBackFn(eventObj);
}

