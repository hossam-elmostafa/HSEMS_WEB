function escSql(v) {
  return String(v ?? '').replace(/'/g, "''");
}

async function getOwnerDepartment(devInterfaceObj) {
  const exec = devInterfaceObj?.executeSQLPromise || devInterfaceObj?.executeSQL;
  if (typeof exec !== 'function') return '';
  try {
    const rs = await exec(`SELECT TOP 1 ISNULL(HSEPLC_OWNRDPRT,'') AS OWNERDPT FROM HSE_HSEPLC`);
    const row = rs?.recordset?.[0] ?? rs?.[0]?.recordset?.[0] ?? rs?.[0];
    return String(row?.OWNERDPT ?? row?.ownerdpt ?? '').trim();
  } catch (e) {
    console.warn('[Web_HSE] CAR Entry owner department lookup:', e);
    return '';
  }
}

async function getUserDepartment(devInterfaceObj, userObj) {
  const exec = devInterfaceObj?.executeSQLPromise || devInterfaceObj?.executeSQL;
  if (typeof exec !== 'function') return '';
  const login =
    (typeof devInterfaceObj?.getUserName === 'function' ? devInterfaceObj.getUserName() : '') ||
    userObj?.LogIn ||
    userObj?.LOGIN ||
    '';
  const userLogin = String(login).trim();
  if (!userLogin) return '';

  try {
    const sql = `SELECT TOP 1 ISNULL(EMPLOYEE_WSHOP,'') AS EMPDEP FROM CMN_EMPLOYEE WHERE EMPLOYEE_USRID = '${escSql(userLogin)}' OR EMPLOYEE_LOGINNAME = '${escSql(userLogin)}'`;
    const rs = await exec(sql);
    const row = rs?.recordset?.[0] ?? rs?.[0]?.recordset?.[0] ?? rs?.[0];
    return String(row?.EMPDEP ?? row?.empdep ?? '').trim();
  } catch (e) {
    console.warn('[Web_HSE] CAR Entry user department lookup:', e);
    return '';
  }
}

function cleanScalar(v) {
  if (v == null) return '';
  const s = String(v).trim();
  if (s === 'undefined' || s === 'null' || s === 'NaN') return '';
  return s;
}

/** Keep `eventObj.fullRecord` aligned with FormSetField (React state can lag behind save payload). RQ_HSE_4_4_25_18_22 */
function mergeIntoFullRecord(eventObj, fieldLogical, value) {
  const row = eventObj?.fullRecord;
  if (!row || typeof row !== 'object' || value == null || cleanScalar(value) === '') return;
  const u = fieldLogical.toUpperCase();
  const k = Object.keys(row).find((key) => key.toUpperCase() === u) || u;
  row[k] = value;
}

/**
 * C++ NewCarEntryCategory::DisplayToolBarButtonClicked NEW + iComplete==1 for HSE_TGCRENTRY:
 * default site from policy, user department on HSE_CRENTRY (RQ-SF-9-1-23_2_4, RQ_SF_15_3_23_1).
 * RQ_HSE_4_4_25_18_22
 */
async function applyNewCarEntryMainDefaults(devInterfaceObj, eventObj) {
  const { executeSQLPromise, FormSetField } = devInterfaceObj || {};
  if (typeof FormSetField !== 'function' || typeof executeSQLPromise !== 'function') return;

  try {
    const siteData = await executeSQLPromise(`EXECUTE GetPolicyValue 'HSE_HSEPLC','HSEPLC_DFLST'`);
    const r0 = siteData?.recordset?.[0];
    const site = r0 ? cleanScalar(Object.values(r0)[0]) : '';
    if (site) {
      FormSetField('HSE_CRENTRY', 'CRENTRY_ST', site, 'scr');
      try {
        FormSetField('HSE_TgCrEntry', 'CRENTRY_ST', site, 'scr');
      } catch (_) {
        /* ignore */
      }
      mergeIntoFullRecord(eventObj, 'CRENTRY_ST', site);
    }
  } catch (e) {
    console.warn('[Web_HSE] CAR Entry NEW default site (desktop parity):', e);
  }

  const userDept = await getUserDepartment(devInterfaceObj, eventObj || {});
  if (userDept) {
    FormSetField('HSE_CRENTRY', 'CRENTRY_DPR', userDept, 'scr');
    try {
      FormSetField('HSE_TgCrEntry', 'CRENTRY_DPR', userDept, 'scr');
    } catch (_) {
      /* ignore */
    }
    mergeIntoFullRecord(eventObj, 'CRENTRY_DPR', userDept);
  }
}

